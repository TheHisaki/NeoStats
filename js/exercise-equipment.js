// Function to get equipment attributes for an exercise
function getExerciseEquipment(exerciseName, muscleGroup) {
  if (
    exerciseEquipment[muscleGroup] &&
    exerciseEquipment[muscleGroup][exerciseName]
  ) {
    return exerciseEquipment[muscleGroup][exerciseName];
  }
  // Default: no equipment specified
  return {
    poulie: false,
    machine: false,
    poidsDeCorps: false,
    sansMateriel: false,
  };
}

// Function to check if an exercise matches equipment filter
function exerciseMatchesEquipmentFilter(exerciseName, muscleGroup, filter) {
  if (!filter || filter === "all") {
    return true;
  }

  const equipment = getExerciseEquipment(exerciseName, muscleGroup);
  return equipment[filter] === true;
}
